namespace c_string_escape_character;

class Program
{
    static void Main(string[] args)
    {
        // escape characters
        // 1. this will print sanothimi in the new line
        Console.WriteLine("Hello\nSanothimi");

        // 2. this will provide a tab between the words
        Console.WriteLine("Hello\tSanothimi");

        // 3. lets you to print something with double quote
        Console.WriteLine("\"HelloSanothimi\"");

        // 4. to print path using slash
        Console.WriteLine("D:\\M3_VP\\I_Strings\\c_string_escape_character\\Program.cs");
    }
}
